from turtle import Turtle
# Inicializar uma turtle
t = Turtle()
# Definir a velocidade, que varia de 1 a 10
t.speed(1)
# Movimentar a turtle para frente, em pixels
t.forward(100)
# Movimentar a turtle x graus para a direita
t.right(90)
t.forward(100)
# Rotacionar em x graus a turtle para a esquerda
t.left(90)
t.forward(100)
# Movimentar a turtle para t≈ïas
t.backward(200)
